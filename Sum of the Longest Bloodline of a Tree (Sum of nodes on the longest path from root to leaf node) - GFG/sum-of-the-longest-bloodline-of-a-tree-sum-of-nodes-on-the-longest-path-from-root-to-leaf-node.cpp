class Solution
{
    private:
    void maxLongSum(Node* root,int sum,int len,int &maxLen,int &maxSum){
        if(root==NULL){
            if(len>maxLen){
                maxLen=len;
                maxSum=sum;
                return;
            }
            else if(len==maxLen){
                maxSum=max(sum,maxSum);
                return;
            }
            else return;
        }
        sum=sum+root->data;
        maxLongSum(root->left,sum,len+1,maxLen,maxSum);
        maxLongSum(root->right,sum,len+1,maxLen,maxSum);
        
    }
public:
    
    int sumOfLongRootToLeafPath(Node *root)
    {
        //code here
        int sum=0,len=0,maxLen=0,maxSum=0;
        maxLongSum(root,sum,len,maxLen,maxSum);
        return maxSum;
        
    }
};

